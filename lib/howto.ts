// === Helper для построения Schema.org HowTo ===
export function buildHowToSchema(h: HowToDef) {
  return {
    '@type': 'HowTo',
    '@id': `${SITE.url}/blog/${h.articleSlug}/#howto`,
    name: h.name,
    description: h.description,
    totalTime: h.totalTime,
    ...(h.estimatedCost && {
      estimatedCost: {
        '@type': 'MonetaryAmount',
        currency: h.estimatedCost.currency,
        value: h.estimatedCost.value,
      },
    }),
    ...(h.supplies && { supply: h.supplies.map((s) => ({ '@type': 'HowToSupply', name: s })) }),
    ...(h.tools && { tool: h.tools.map((t) => ({ '@type': 'HowToTool', name: t })) }),
    step: h.steps.map((s, i) => ({
      '@type': 'HowToStep',
      position: i + 1,
      name: s.name,
      text: s.text,
      ...(s.imageUrl && { image: s.imageUrl }),
    })),
  };
}

export function getHowToForArticle(slug: string): HowToDef | undefined {
  return HOWTOS.find((h) => h.articleSlug === slug);
}
